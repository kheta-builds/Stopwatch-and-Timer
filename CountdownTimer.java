    @SuppressWarnings("unchecked")
    Timer timer = new Timer(1000, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            countDownSeconds--;
            if (countDownSeconds <= 59 && countDownSeconds >= 10) {
                secondsValue.setText(String.valueOf(countDownSeconds));
            }
            else if (countDownSeconds < 10 && countDownSeconds >= 1) {
                secondsValue.setText("0" + countDownSeconds);
            }
            else{
                secondsValue.setText("0"+countDownSeconds);
                if(countDownHours >= 0 && countDownMinutes > 0){
                    countDownSeconds = 60;
                    countDownMinutes--;
                    if(countDownMinutes <= 59 && countDownMinutes >= 10){
                        minutesValue.setText(String.valueOf(countDownMinutes));
                    }
                    else if(countDownMinutes < 10){
                        minutesValue.setText("0" + countDownMinutes);
                    }
                    
                }
                else if(countDownHours > 0 && countDownMinutes == 0){
                    countDownHours--;
                    if(countDownHours <= 59 && countDownHours >= 10){
                        hoursValue.setText(String.valueOf(countDownHours));
                    }
                    else if(countDownHours < 10){
                        hoursValue.setText("0" + countDownHours);
                    }
                    countDownMinutes = 60;
                }
            }
            if(countDownHours == 0 && countDownMinutes == 0 && countDownSeconds == 0){
               timer.stop();
               JOptionPane.showMessageDialog(null,"TIME IS UP!","ALARM RINGING",INFORMATION_MESSAGE);
            }
        }
    });
    
