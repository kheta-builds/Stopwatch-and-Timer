    private void startActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startActionPerformed

        try {
            char a = hoursValue.getText().charAt(1);
            char b = minutesValue.getText().charAt(1);
            char c = secondsValue.getText().charAt(1);

            if (hoursValue.getText().charAt(0) == '0') {
                countDownHours = a - '0';
                
            } else {
                countDownHours = Integer.parseInt(hoursValue.getText());
            }
            if (minutesValue.getText().charAt(0) == '0') {
                countDownMinutes = b - '0';
                
            } else {
                countDownMinutes = Integer.parseInt(minutesValue.getText());
            }
            if (secondsValue.getText().charAt(0) == '0') {
                countDownSeconds = c - '0';

            } else {
                countDownSeconds = Integer.parseInt(secondsValue.getText());
            }

        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Something went wrong!", "MESSAGE", ERROR_MESSAGE);
        }
        timer.start();

    }//GEN-LAST:event_startActionPerformed

    private void pauseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pauseActionPerformed
        if(pause.getText().equals("Pause")){
           pause.setText("resume");
           timer.stop();
        }
        else if(pause.getText().equals("resume")){
            pause.setText("Pause");
            timer.restart();
        }
    }//GEN-LAST:event_pauseActionPerformed

